// Package sandbox обеспечивает изолированное тестовое выполнение flows.
//
// Sandbox позволяет:
//   - Запускать flow в изолированном режиме
//   - Мокать или записывать внешние вызовы
//   - Сравнивать результаты с эталонными
//   - Проверять изменения перед применением в прод
//
// Режимы работы:
//   - mock   — все внешние вызовы мокаются
//   - record — вызовы выполняются и записываются
//   - replay — вызовы сравниваются с записанными
//   - live   — реальные вызовы с пометкой sandbox
package sandbox
